package net.sourceforge.plantuml.communicationdiagram.link;

/**
 * Different orientations links can have in a Communication Diagram.
 *
 * @author Carlos Gomez
 */
enum Orientation {

  /**
   * Links have horizontal orientation when their message(s) flow around the horizontal axis.
   */
  HORIZONTAL,

  /**
   * Links have vertical orientation when their message(s) flow around the vertical axis.
   */
  VERTICAL;

}
